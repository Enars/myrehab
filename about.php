        <h1>Regler för denna tjänst</h1>
    </div>  
    <div class="ett">
        <h3 id="ett">Användardata</h3>
        <p>Vi använder användardata enbart för att kunna identifiera användaren.
Datan kommer ej spridas vidare till andra tjänster eller leverantörer eller för andra kommersiella ändamål. Vi håller oss inom reglerna för PUL och kommer agera enligt GDPR när den träder i kraft.

        </p>
    </div>
    <div class="two">
        <h3 id="two">Sociala regler</h3>
        <p>Användare är själva ansvariga för kommentarer och inlägg som postas.</p>
        <p>Följs inte dessa villkor kan användaren stängas av från tjänsten.</p>
    </div> 
    <div class="ett">
        <h3>Inget Irrelevant</h3>
        <p>Webbsidan ska enbart användas för relevanta syften.<br>Vi vill t.ex. inte se bilder på din kattunge i ett inlägg som handlar om benfrakturer.</p>
    </div> 
    <div class="two">
        <h3>Ingen skräppost</h3>
        <p>Massutskick av kommentarer och inlägg uppskattas inte och får inte förekomma.
        Om massutskick görs kommer de tas bort och användaren blir kontaktad.
        </p>
    </div> 
    <div class="ett">
        <h3>Kommersiella budskap och reklam</h3>
        <p>Är inte tillåtet i inlägg och kommentarer.
        Allt som kan ses som reklam kommer att tas bort från sidan och
        den som postat det kommer bli kontaktad.
    </p>
    </div> 
    <div class="two">
        <h3>Inget hat</h3>
        <p>Det är ok att kritisera, dock så är det inte ok att hoppa på personer bara för att de inte delar samma åsikter som dig.</p>
    </div> 
    <div class="ett">
        <h3>Inget utgivande av känslig information</h3>
        <p>Det är inte tillåtet att ge ut känslig information som kan anses som intrång i en annan person privatliv som t.ex. personnummer, adress, telefonnummer.</p>
    </div> 
    <div class="two">
        <h3>Trakasserier och förtal</h3>
        <p>Det är inte tillåtet att publicera uppgifter som kan peka ut användare som brottsliga, nedsättande ord och trakasserier.
    </div> 
    <div class="ett">
        <h3>Diskriminering</h3>
        <p>Diskriminering i form av sexism, rasism eller hets mot folkgrupp är inte tillåtet.</p>
    </div> 
    <div class="two">
        <h3>Pornografi och olika våldsskildringar.</h3>
        <p>Är inte tillåtet på webbsidan.</p>
    </div> 
    <div class="ett">
            <h3>Upphovsrättsligt skyddat material</h3>
        <p>Du får inte publicera innehåll som du inte har rätt att publicera.</p>
    </div> 
    <div class="two">
        <h3>Länkar och referenser</h3>
        <p>Det är inte tillåtet att länka till andra sidor som kan innehålla otillåtet material som nämns ovan.
        Relevanta bilder på din skada som behandlas tillåts.
    </div> 
    <div class="ett">
        <h3>Minderåriga på webbsidan</h3>
        <p>Personer som är under 18 år måste ha tillstånd av målsman.
        Saknas tillåtelse kommer användaren stängas av från sidan tills tillåtelse meddelats av målsman.
        </p>
    </div> 
    <div class="two">
    <h3>Vad händer om jag bryter mot dessa regler?</h3>
    <p>Individer som bryter mot dessa regler varnas, vid upprepade förseelser så riskerar användaren att bli avstängd från tjänsten.</p> 
    </div>
